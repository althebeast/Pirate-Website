        <h2 id="konubasligi">Denizcilik Derecesi: <i class="fa-solid fa-star"></i><i class="fa-solid fa-star"></i><i class="fa-solid fa-star"></i> <br>Seydi Ali Reis</h1><br><br>
        <center><img src="images/alireis.jpeg" width="500" alt="Seydi Ali Reis" title="Barbaros Hayrettin Paşa"></center><br>
        <p id="p1">Seydi Ali Reis Galata'da doğdu. İstanbul'un fethinden sonra Sinop'a yerleşen denizci bir ailenin oğluydu. Dedesi ve babası tersane kethüdasıydı. 1522 yılında Rodos seferine katılan Seydi Ali Reis, Barbaros Hayreddin Paşa'nın emrinde bir çok deniz seferine çıktı ve Batı Akdeniz bölgesini çok iyi öğrendi. <br><br>

 

            Preveze Savaşı'ndan sonra adı daha çok duyulmaya başladı. Trablusgarp'ın fethi ile biten harekatta Kaptan-ı Derya Sinan Paşa ve Turgut Reis emrinde çalıştı. Basra'da, bir Osmanlı donanmasını Süveyş'e getirmek için, 1553 yılında Hint Kaptanı tayin edildi. Seydi Ali Reis 34 parçalık Portekiz donanması ile Güney Arabistan sahillerinde karşılaştı. Fırtınaya ve şiddetli düşman taarruzuna rağmen Demen kalesi önüne gelebildi. Burada karaya oturan üç gemiden sonra, elinde kalan altı gemiyle birlikte Güceret'in başkenti Ahmedabat'a gitti. Süveyş'i geçemeyeceğini anlayan Seydi Ali Reis gemileri ve mühimmatı satarak parasını İstanbul'a gönderdi ve üç yıl Osmanlı ülkesi dışında yaşadı. <br><br>
            
             
            
            1557 yılında İstanbul'a döndüğünde, mahvolmuş bir donanmanın sorumlusu olmakla beraber, başına gelen olağanüstü olaylar yüzünden suçlu görülmedi. Önce müteferrika yapıldı. Ardından Diyarbakır tımar defterdarı tayin edildi. Bir süre Şehzade Selim'in hizmetinde çalıştı. Galata Hassa gemi reislerinden biri oldu. 1562 yılında İstanbul'da öldü.
            
            </p>

    
            
            
